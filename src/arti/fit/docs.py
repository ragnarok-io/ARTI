def check_generated_docs(path: str | Path, *, phases: int = 16) -> None:
    """Raise if generated docs are missing or stale."""

    target = Path(path)
    expected = generate_capabilities_markdown(phases=phases)
    existing = target.read_text(encoding="utf-8") if target.exists() else None
    if existing != expected:
        raise ValueError(f"generated docs are out of date: {target}")


