"""Source-backed documentation generation helpers."""

from __future__ import annotations

import json
from importlib.resources import files
from pathlib import Path
from typing import Any

from .metadata import capabilities


FIT_CONFIG_SCHEMA_FILENAME = "fit-config.schema.json"
TASK_GRAPH_SCHEMA_FILENAME = "task-graph.schema.json"


def markdown_table(headers: list[str], rows: list[list[Any]]) -> list[str]:
    lines = [
        "| " + " | ".join(headers) + " |",
        "| " + " | ".join("---" for _ in headers) + " |",
    ]
    for row in rows:
        lines.append("| " + " | ".join(str(value) for value in row) + " |")
    return lines


def generate_capabilities_markdown(*, phases: int = 16) -> str:
    """Generate the source-backed capabilities reference page."""

    caps = capabilities(phases=phases)
    lines = [
        "# Capabilities Reference",
        "",
        "<!-- Generated by `arti docs generate`. Do not edit by hand. -->",
        "",
        f"Generated from `arti.capabilities(phases={phases})`.",
        "",
        "## Profiles",
        "",
    ]
    profile_rows = []
    for name, profile in sorted(caps["profiles"].items()):
        profile_rows.append(
            [
                f"`{name}`",
                f"`{profile['observer_phase']}`",
                f"`{profile['virtual_recall']}`",
                f"`{profile['coord_dim']}`",
                f"`{profile['coord_frame_mode']}`",
            ]
        )
    lines.extend(markdown_table(["Name", "Observer Phase", "Virtual Recall", "Coord Dim", "Coord Frame"], profile_rows))
    lines.extend(["", "## Scales", ""])
    scale_rows = []
    for name, scale in sorted(caps["scales"].items()):
        scale_rows.append(
            [
                f"`{name}`",
                f"`{scale['hidden_multiplier']}`",
                f"`{scale['operator_count']}`",
                f"`{scale['interface_slots']}`",
                f"`{scale['recall_slots']}`",
                f"`{scale['recall_steps']}`",
                f"`{scale['recall_activation']}`",
            ]
        )
    lines.extend(markdown_table(["Name", "Hidden Multiplier", "Operators", "Interface Slots", "Recall Slots", "Recall Steps", "Recall Activation"], scale_rows))
    lines.extend(["", "## Plugins", ""])
    plugin_rows = []
    for name, plugin in sorted(caps["plugins"].items()):
        plugin_rows.append(
            [
                f"`{name}`",
                f"`{plugin['kind']}`",
                f"`{plugin['available']}`",
                ", ".join(f"`{item}`" for item in plugin["capabilities"]),
            ]
        )
    lines.extend(markdown_table(["Name", "Kind", "Available", "Capabilities"], plugin_rows))
    lines.extend(
        [
            "",
            "## Raw JSON",
            "",
            "```json",
            json.dumps(caps, indent=2, sort_keys=True),
            "```",
            "",
        ]
    )
    return "\n".join(lines)


